// Table of 1
for (let a = 1; a <= 10; a++) {
    console.log("1 * " + a + " = " + a * 1);
}

// Table of 2
let b = 1;
while (b <= 10) {
    console.log("2 * " + b + " = " + b * 2)
    b++
}

// Table of 3
for (let c = 1; c <= 10; c++) {
    console.log("3 * " + c + " = " + c * 3);
}
// Table of 5
let d = 10;
while (d >= 1) {
    console.log("5 * " + d + " = " + d * 5)
    d--
} 